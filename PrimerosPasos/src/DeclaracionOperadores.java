
public class DeclaracionOperadores {

	public static void main(String[] args)
	{
	  //con la palabra reserveda final
	// se declaran constantes	
       final int c=5;
       
      int resultado;
      
      resultado=5;
      
      resultado=resultado*c;
       
       System.out.print("EL VALOR DE RESULTADO ES " + resultado);
       
       

	}

}
